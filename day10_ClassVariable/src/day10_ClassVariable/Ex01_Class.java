package day10_ClassVariable;

public class Ex01_Class {

	public int sumFunc(int n1, int n2) {return n1 + n2;}
	public int sumFunc(int n1, int n2, int n3) {
		return n1 + n2 + n3;
	}
	public double sumFunc(int n1, double n2) {return n1 + n2;}
	public String sumFunc(String n1, String n2) {return n1 + n2;}
}
